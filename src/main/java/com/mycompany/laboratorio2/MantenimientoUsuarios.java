    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
   String usuario = txtUsuario.getText().trim();
        String clave = txtClave.getText();
        String confirmar = txtConfirmarClave.getText();
        
        if (usuario.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingresa un usuario");
            return;
        }
        
        if (!validarClave(clave, confirmar)) {
            return;
        }
        
        // Verifica que no exista
        for (Usuario u : Usuario.listaUsuarios) {
            if (u.getNombreUsuario().equals(usuario)) {
                JOptionPane.showMessageDialog(this, "El usuario ya existe");
                return;
            }
        }
        
        Usuario nuevo = new Usuario(usuario, clave, chkActivo.isSelected());
        Usuario.listaUsuarios.add(nuevo);
        
        JOptionPane.showMessageDialog(this, "Usuario guardado");
        cargarTabla();
        limpiar();
        
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
 if (filaSeleccionada < 0) {
            JOptionPane.showMessageDialog(this, "Selecciona un usuario de la tabla");
            return;
        }
        
        String nuevaClave = txtClave.getText();
        String confirmar = txtConfirmarClave.getText();
        
        // Si escribió algo en clave, valida
        if (!nuevaClave.isEmpty() && !validarClave(nuevaClave, confirmar)) {
            return;
        }
        
        Usuario u = Usuario.listaUsuarios.get(filaSeleccionada);
        if (!nuevaClave.isEmpty()) {
            u.setClave(nuevaClave);
        }
        u.setActivo(chkActivo.isSelected());
        
        JOptionPane.showMessageDialog(this, "Usuario modificado");
        cargarTabla();
        limpiar();
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnInactivarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInactivarActionPerformed
 if (filaSeleccionada < 0) {
            JOptionPane.showMessageDialog(this, "Selecciona un usuario de la tabla");
            return;
        }
        
        Usuario u = Usuario.listaUsuarios.get(filaSeleccionada);
        u.setActivo(false);
        
        JOptionPane.showMessageDialog(this, "Usuario inactivado");
        cargarTabla();
        limpiar();
   
    }//GEN-LAST:event_btnInactivarActionPerformed

