    private void cargarTabla() {
        modeloTabla.setRowCount(0); 
        for (Usuario u : Usuario.listaUsuarios) {
            String estado = u.isActivo() ? "Activo" : "Inactivo";
            modeloTabla.addRow(new Object[]{u.getNombreUsuario(), estado});
        }
    }
    
    private boolean validarClave(String clave, String confirmar) {
        if (!clave.equals(confirmar)) {
            JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden"); 
            return false;
        }
        if (clave.length() < 13) {
            JOptionPane.showMessageDialog(this, "Mínimo 13 caracteres"); 
            return false;
        }
        if (!clave.matches(".*[A-Z].*")) {
            JOptionPane.showMessageDialog(this, "Debe tener 1 mayúscula"); 
            return false;
        }
        if (!clave.matches(".*[!@#$%^&*()_+\\-=\\[\\]{};':\"\\\\|,.<>\\/?].*")) {
            JOptionPane.showMessageDialog(this, "Debe tener 1 signo especial"); 
            return false;
        }
        return true;
    }

